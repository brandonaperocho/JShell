package a2;
/**
 * 
 * <h1> Class Node </h1>
 * Node class is used in LinkedList. Has element to store data and next to get
 * the next Node in line.
 *
 */
public class Node {

  /**
   * Tail of the Node
   */
  private Node next; // Tail of the Node
  /**
   * Data saved into element, usually a String
   */
  private Object element; // Data of the Node

  /**
   * Constructor 
   * 
   * @param e --> Saves the element into the Node element
   */
  public Node (Object e) {
    element = e;
    next = null;
  }

  /**
   * 
   * @return Element from the Node
   */
  public Object getElement() {
    return element;
  }
  /**
   * 
   * @param data Sets the Node's element to data
   */
  public void setElement(Object data) {
    element = data;
  }
  /**
   * 
   * @return The Nodes tail; a Node
   */
  public Node getTail() {
    return next;
  }
  /**
   * 
   * @param tailNode --> Sets the tail of Node to tailNode
   */
  public void setTail(Node tailNode) {
    next = tailNode;
  }
}
